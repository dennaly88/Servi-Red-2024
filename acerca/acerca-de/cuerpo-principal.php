            Venezolana de Televisión (VTV) es un canal de televisión abierta venezolano. Oficialmente estrenado el 1 de agosto de 1964 como un canal de propiedad privada con otros intereses, fue estatizado el 8 de abril de 1976.

VTV ha producido varias telenovelas, incluyendo títulos como Ifigenia, Doña Perfecta y La Dueña de 1984 que quizás fue quizás su producción más exitosa y popular.1​

Desde la llegada de Hugo Chávez al poder en 1999, el canal tiene una línea editorial de propaganda política a favor del gobierno bolivariano, ahora presidido por Nicolás Maduro.2​3​4​ VTV ha sido utilizada por el gobierno para hacer campaña contra la oposición de Venezuela y los medios de comunicación privados de Venezuela. En 2004, VTV produjo otra telenovela, Amores de Barrio Adentro, pero solo se veía una vez a la semana y duró solo unos meses.5​

La mayor parte de su programación es grabada en vivo, con noticiarios a favor del gobierno que censura actos perpetrados por la oposición y las protestas en contra del régimen, según lo reportan diversos medios.2​3​4​6​ Además, también retransmite programación de Telesur y de RT.

Venezolana de Televisión fue fundada como una emisora privada de alcance local en la ciudad de Caracas, por la familia de empresarios Vollmer, comenzando sus transmisiones en la frecuencia 8 en VHF (180-186 MHz), bajo el nombre inicial de Cadena Venezolana de Televisión, C.A. (CVTV), el 1 de agosto de 1964 a las 07:30 p. m.7​

El entonces presidente de la República Raúl Leoni fue el elegido para cortar la cinta inaugural. Pese a su nombre, sin embargo, no era una cadena nacional de televisión pues carecía de alcance nacional, transmitiendo en el área de Caracas durante sus primeros años.8​ Esta situación fue cambiando y para finales de la década de 1960 la señal ya contaba con repetidoras en las principales ciudades del país y se convirtió en la primera cadena en producir y transmitir Televisión en color en 1972 con el documental Churún Merú de Renny Ottolina. Así, el primer logotipo de VTV fue el nombre de la empresa (CVTV) en un número 8 (reflejando su número de canal en Caracas).

